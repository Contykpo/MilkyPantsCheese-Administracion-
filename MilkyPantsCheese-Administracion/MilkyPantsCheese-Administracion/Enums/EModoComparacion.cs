namespace MilkyPantsCheese
{
	/// <summary>
	/// Enum que representa un metodo de comparacion de valores numericos
	/// </summary>
	public enum EModoComparacion
	{
		/// <summary>
		/// El valor debe ser exaxtamento igual
		/// </summary>
		Exacto,

		/// <summary>
		/// El valor comparado debe ser menor
		/// </summary>
		Menor,

		/// <summary>
		/// El valor comparado debe ser mayor
		/// </summary>
		Mayor
	}
}
